/* ================= exists ================= */

async function jobExists(
  COMPANY_CODE: string,
  JOB_TYPE: string,
  JOB_NO: string,
  conn: oracledb.Connection
): Promise<boolean> {
  const sql = `
    SELECT COUNT(*) CNT
    FROM TI_JOB
    WHERE COMPANY_CODE = :COMPANY_CODE
      AND JOB_TYPE = :JOB_TYPE
      AND JOB_NO = :JOB_NO
  `;
  const r: any = await oracleDb.query(sql, { COMPANY_CODE, JOB_TYPE, JOB_NO }, conn);
  return (r.rows?.[0]?.CNT || 0) > 0;
}

