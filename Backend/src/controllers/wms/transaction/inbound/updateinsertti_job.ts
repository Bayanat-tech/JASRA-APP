/* ================= UPSERT ================= */

export async function upsertTIJob(data: any) {
  return oracleDb.withTransaction(async (conn) => {
    const exists = await jobExists(data.COMPANY_CODE, data.JOB_TYPE, data.JOB_NO, conn);
    if (exists) {
      await updateTIJob(data, conn);
      return "UPDATED";
    } else {
      await insertTIJob(data, conn);
      return "INSERTED";
    }
  });
}

/* ================= HANDLER ================= */

export const upsertTIJobHandler = async (req: Request, res: Response) => {
  try {
    const action = await upsertTIJob(req.body);
    res.status(constants.STATUS_CODES.OK).json({
      success: true,
      action,
      message: `TI_JOB ${action} successfully`
    });
  } catch (e: any) {
    res.status(500).json({
      success: false,
      message: e.message || "TI_JOB upsert failed"
    });
  }
};
